package ar.edu.info.unlp.ejercicioDemo;

public class Ingrediente {
    
    private String nombre;
    private double costo;

    public Ingrediente(String nombre, double costo) {
        this.nombre = nombre;
        this.costo = costo;
    }

    public String getNombre() {
        return nombre;
    }

    public double costo() {
        return costo;
    }

}
